  li  x21,0
  li  x22,0
  li  x23,0
  li  x24,0
  li  x25,0
  li  x26,0
  li  x27,0
  li  x28,0
  li  x29,0
  li  x30,0
  li  x31,0

  # initialize global pointer
.option push
.option norelax
  la gp, _gp
.option pop

  # Set stack pointer
  la sp, __stack

  call main

  #upon exiting 'main' hang ...
hang:
  j hang

